function scheduledCareTaskCanCompleteFromDailyLog(task = {}) {
  return Boolean(task?.id && !task.removed && task.status !== "Completed" && task.status !== "Cancelled" && task.dogType === "ownedDog");
}

function scheduledCareTaskMatchesDailyLog(task = {}, log = {}) {
  if (!scheduledCareTaskCanCompleteFromDailyLog(task)) return false;
  if (String(log.source || "") === "taskScheduler") return false;
  const logDate = dateOnly(log.date || log.loggedAt || "");
  if (!logDate || dateOnly(task.date || "") !== logDate) return false;
  if (scheduledCareCompletionTypeKey(task.activityType || task.title) !== scheduledCareCompletionTypeKey(log.careType || log.category || log.type)) return false;
  return String(task.dogId || task.sourceDogId || "") === String(log.dogId || "");
}

function scheduledCareTaskSortForDailyLog(left = {}, right = {}) {
  const leftAuto = left.source === OWNED_DOG_NEXT_BATH_TASK_SOURCE ? 0 : left.autoGenerated ? 1 : 2;
  const rightAuto = right.source === OWNED_DOG_NEXT_BATH_TASK_SOURCE ? 0 : right.autoGenerated ? 1 : 2;
  return leftAuto - rightAuto ||
    String(left.startTime || "").localeCompare(String(right.startTime || "")) ||
    String(left.updatedAt || left.submittedAt || "").localeCompare(String(right.updatedAt || right.submittedAt || ""));
}

function scheduledCareTaskForDailyLog(log = {}) {
  if (String(log.source || "") === "taskScheduler") return null;
  const directTaskId = log.scheduledCareTaskId || "";
  if (directTaskId) {
    const directTask = scheduledCareTasks().find((task) => task.id === directTaskId && scheduledCareTaskCanCompleteFromDailyLog(task));
    if (directTask) return directTask;
  }
  return scheduledCareTasks()
    .filter((task) => scheduledCareTaskMatchesDailyLog(task, log))
    .sort(scheduledCareTaskSortForDailyLog)[0] || null;
}

