async function syncBoardingStayMilestoneTasksForRecord(record = {}, options = {}) {
  const displayRecord = boardingDogRecordForDisplay(record.id || "") || record;
  if (!displayRecord?.id) return false;
  const desiredKeys = new Set();
  const changesById = new Map();
  const now = new Date().toISOString();
  const staff = staffIdentity();

  arrayValue(displayRecord.stays).forEach((stay) => {
    if (!boardingServiceTaskStayIsSchedulable(displayRecord, stay)) return;
    boardingStayMilestoneDefinitions(displayRecord, stay).forEach((milestone) => {
      const sourceKey = boardingStayMilestoneSourceKey(displayRecord, stay, milestone.type);
      if (!sourceKey) return;
      desiredKeys.add(sourceKey);
      const candidates = boardingStayMilestoneTaskCandidates(displayRecord, stay, milestone, sourceKey);
      const keep = preferredBoardingStayMilestoneTask(candidates);
      candidates.filter((task) => task.id !== keep?.id && task.status !== "Completed").forEach((duplicate) => {
        queueBoardingServiceTaskChange(changesById, upsertRecord("scheduledCareTask", {
          ...duplicate,
          removed: true,
          removedAt: now,
          removedBy: staff.name || "System",
          removedReason: "duplicate boarding stay milestone auto task",
          updatedAt: now,
        }));
      });
      const draft = boardingStayMilestoneScheduledTaskPayload(displayRecord, stay, milestone, keep || {});
      if (!keep || boardingStayMilestoneTaskNeedsUpdate(keep, draft)) {
        queueBoardingServiceTaskChange(changesById, upsertRecord("scheduledCareTask", draft));
      }
    });
  });

  boardingStayMilestoneAutoTasksForRecord(displayRecord).forEach((task) => {
    if (task.status === "Completed") return;
    if (!task.sourceKey || desiredKeys.has(task.sourceKey)) return;
    if (!boardingStayMilestoneSourceKeyStillNeeded(displayRecord, task.sourceKey)) {
      queueBoardingServiceTaskChange(changesById, upsertRecord("scheduledCareTask", {
        ...task,
        removed: true,
        removedAt: now,
        removedBy: staff.name || "System",
        removedReason: "boarding stay milestone no longer scheduled",
        updatedAt: now,
      }));
    }
  });

  const changes = [...changesById.values()];
  if (!changes.length) return false;
  if (typeof sendPayloadBatch === "function") {
    await sendPayloadBatch(changes);
  } else {
    for (const payload of changes) await sendPayload(payload);
  }
  if (options.render && activePageId() === "taskSchedulerPage") renderTaskScheduler();
  return true;
}

