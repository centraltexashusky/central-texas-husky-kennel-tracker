async function syncOwnedDogBathTask(dog = {}) {
  if (!dog?.id) return false;
  const { clean, lastBath, intervalDays, nextBathDate } = ownedDogNextBathTaskSchedule(dog);
  let existingAutoTask = activeOwnedDogNextBathTask(clean);
  let sourceChangedForManualOverride = false;
  if (!nextBathDate || clean.removed) {
    return retireDuplicateOwnedDogNextBathTasks(clean, "");
  }
  if (existingAutoTask && lastBath && dateOnly(existingAutoTask.date || "") === dateOnly(lastBath)) {
    const bathLog = latestOwnedDogBathLogForDate(clean, lastBath) || {};
    await completeScheduledCareTaskFromCareLog(existingAutoTask, {
      ...bathLog,
      id: bathLog.sourceCareLogId || bathLog.id || existingAutoTask.careLogId || "",
      dogId: clean.id,
      dogName: ownedDogDisplayName(clean),
      careType: "Bath",
      date: lastBath,
      completedBy: bathLog.completedBy || "Staff",
      completedEmail: bathLog.completedEmail || bathLog.completedByEmail || "",
    });
    existingAutoTask = activeOwnedDogNextBathTask(clean);
  }
  if (existingAutoTask?.sourceManualOverride) {
    const sourceChanged = existingAutoTask.sourceLastBath !== lastBath ||
      Number(existingAutoTask.sourceBathIntervalDays || 0) !== Number(intervalDays || 0) ||
      existingAutoTask.sourceBathDate !== nextBathDate;
    if (!sourceChanged) return retireDuplicateOwnedDogNextBathTasks(clean, existingAutoTask.id);
    sourceChangedForManualOverride = true;
  }

  const sameDateTask = existingOwnedDogBathTaskOnDate(clean, nextBathDate, existingAutoTask?.id || "");
  if (sameDateTask && !isOwnedDogNextBathTask(sameDateTask, clean.id)) {
    const changed = sourceChangedForManualOverride ? await retireOwnedDogNextBathTask(existingAutoTask, { force: true }) : false;
    return await retireDuplicateOwnedDogNextBathTasks(clean, "") || changed;
  }

  const nextPayload = ownedDogNextBathTaskPayload(clean, existingAutoTask || {});
  const changed = !existingAutoTask ||
    existingAutoTask.date !== nextPayload.date ||
    existingAutoTask.startTime !== nextPayload.startTime ||
    Number(existingAutoTask.durationMinutes || 0) !== nextPayload.durationMinutes ||
    existingAutoTask.dogName !== nextPayload.dogName ||
    existingAutoTask.title !== nextPayload.title ||
    existingAutoTask.activityType !== nextPayload.activityType ||
    existingAutoTask.assignedToName !== nextPayload.assignedToName ||
    existingAutoTask.notes !== nextPayload.notes ||
    existingAutoTask.sourceLastBath !== nextPayload.sourceLastBath ||
    Number(existingAutoTask.sourceBathIntervalDays || 0) !== nextPayload.sourceBathIntervalDays;
  if (!changed) return retireDuplicateOwnedDogNextBathTasks(clean, existingAutoTask.id);
  const saved = upsertRecord("scheduledCareTask", nextPayload);
  await sendPayload(saved);
  await retireDuplicateOwnedDogNextBathTasks(clean, saved.id);
  return true;
}

